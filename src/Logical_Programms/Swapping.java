package Logical_Programms;

public class Swapping { //user logic class
	
public static void main(String[] args) { //main method
	//using third variable
	
System.out.println("BEFORE SWAPPING");
	  int a=11;
	  int b=21;
	  System.out.println("a is"+a+"and b is"+b); //concatenation
	
	  int c=a; //use of third variable 'c'
	  a=b;
	  b=c;
	  System.out.println("AFTER SWAPPING");
	  System.out.println("a is"+a+"b is"+b);  //concatenation " string"+character
	  
	  
	  }//main method ends

}//user logic class ends
