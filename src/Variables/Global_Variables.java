package Variables;

public class Global_Variables { //CLASS BODY OPENS
	
	static int i=10;   //Global Variables declared & initialized
	static char ch='M';
	static String s="VELOCITY";
	static boolean b=false;
	static float f=0.001122f;

public static void main(String[]args) { //MAIN METHOD BODY START
	
	System.out.println(i);
	System.out.println(ch);
	System.out.println(s);
	System.out.println(b);
	System.out.println(f);
	
} // MAIN METHOD BODY CLOSES

} //CLASS BODY CLOSES
