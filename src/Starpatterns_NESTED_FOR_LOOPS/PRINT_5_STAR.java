package Starpatterns_NESTED_FOR_LOOPS;

public class PRINT_5_STAR { //ULC
public static void main(String[] args) {//MAIN METHODS START
	
	for(int i=1;i<=5;i++) {
		System.out.print("*");
		//printing in same line
	}
	
	for(int i=1;i<=5;i++) {
		System.out.println("*");
		//printing in different line
	}
	
	
}

}
