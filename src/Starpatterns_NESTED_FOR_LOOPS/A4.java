package Starpatterns_NESTED_FOR_LOOPS;

public class A4 {//user logic class
	
public static void main(String[] args) { //main method
	
for(int i=1;i<=4;i++) //outer for loop ROWS
         {
	      for(int j=1;j<=4;j++) //inner for loop COLUMNS
	         {
	    	  System.out.print("*");
	    	  
	         } //inner loop ends
	    	  
	    System.out.println();	  
         } //outer loop ends

}//main method ends

}//class A4_4 ends
