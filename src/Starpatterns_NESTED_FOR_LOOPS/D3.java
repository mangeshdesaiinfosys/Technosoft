package Starpatterns_NESTED_FOR_LOOPS;

public class D3 { //ULC
	
public static void main(String[] args) { //main method
	for(int i=1;i<=3;i++) {
		for (int j=1;j<=4;j++) {
			if(i==1||i==3||j==2||j==3) {
				System.out.print("*");
			}
			else {
				System.out.print(" ");
			}
		}
		System.out.println();
	}
	
	
	
	
	
	
	
}

}
