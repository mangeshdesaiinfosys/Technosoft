package Casting;

public class primitive {
	public static void main(String[] args) {
		double d=10;
		System.out.println(d);
		
		double d2=1;
		System.out.println(d2);
		
		int i=5;
		double d1=i;
		System.out.println(d1);
		
		int i1=(int)9.5; //type cast operator
		System.out.println(i1);
	}

}
